    private void bnForgotPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnForgotPasswordActionPerformed

        String login = JOptionPane.showInputDialog(this, "Entrez votre login :");

        if (login != null && !login.isEmpty()) {
            UserService userService = new UserService();
            User user = userService.findUserByLogin(login);

            if (user != null) {
                String securitAnswer = JOptionPane.showInputDialog(this,
                        "" + user.getQuestionSecurit() + "\n\nEntrez votre r√©ponse :");

                if (securitAnswer != null && !securitAnswer.isEmpty()) {
                    
                    if (userService.resetPasswordBySecurityQuestion(login, securitAnswer)) {
                        
                        JOptionPane.showMessageDialog(this, "Un nouveau mot de passe a √©t√© envoy√© √† votre adresse e-mail.");
                    } else {
                        JOptionPane.showMessageDialog(this, "R√©ponse incorrecte √† la question secr√®te.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Veuillez entrer la r√©ponse √† la question secr√®te.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Utilisateur non trouv√©.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez entrer votre login.");
        }
    }//GEN-LAST:event_bnForgotPasswordActionPerformed

