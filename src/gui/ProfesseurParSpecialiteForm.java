        if (specialiteBox.getSelectedIndex() != 0) {
            for (Professeur p : ps.findBySpecialite((String) specialiteBox.getSelectedItem())) {
                model.addRow(new Object[]{p.getId(), p.getNom(), p.getPrenom(), p.getSpecialite()});
            }
        }
