    void loadProf() {
        specialiteBox.addItem("Choisi une option");
        Set<String> specialitesUniques = new HashSet<>();

        for (Professeur p : ps.findAll()) {
            specialitesUniques.add(p.getSpecialite());
        }

        for (String specialite : specialitesUniques) {
            specialiteBox.addItem(specialite);
        }
    }

